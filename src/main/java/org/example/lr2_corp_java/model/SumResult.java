package org.example.lr2_corp_java.model;

public class SumResult {
    private double sum;
    private int count;

    public SumResult(double sum, int count) {
        this.sum = sum;
        this.count = count;
    }
    public double getSum() {
        return sum;
    }
    public int getCount() {
        return count;
    }
}
